class Solution
{
public:
    int height,sum;
    void cal(Node* root, int h)
    {
        
        if(root==NULL)
        return;
        
        if(root->left==NULL&&root->right==NULL)
        {
            if(h>height)
            {
                height=h;
                sum=root->data;
            }else if(h==height)
            {
                if(root->data>sum)
                sum=root->data;
            }
            
            return;
        }
        
        if(root->left)
        {
            root->left->data+=root->data;
            cal(root->left,h+1);
        }
        
        if(root->right)
        {
            root->right->data+=root->data;
            cal(root->right,h+1);
        }
    }
    int sumOfLongRootToLeafPath(Node *root)
    {
        //code here
        height=0,sum=0;
        cal(root,0);
        
        return sum;
    }
};

